SwitchDeviceFormat switchFormatForPortCount(int portCount, {int? validPorts}) {
  if (validPorts != null) {
    return switch (validPorts) {
      <= 30 => const SwitchUD1U30PStacked(),
      32 => const SwitchUD1U32PStacked(),
      34 => const SwitchUD1U34PStacked(),
      36 => const SwitchUD1U36PStacked(),
      38 => const SwitchUD1U38PStacked(),
      40 => const SwitchUD1U40PStacked(),
      42 => const SwitchUD1U42PStacked(),
      44 => const SwitchUD1U44PStacked(),
      46 => const SwitchUD1U46PStacked(),
      _ => const SwitchUD1U48PStacked(),
    };
  }
  return switch (portCount) {
    <= 6 => const SwitchUD1U6P(),
    8 => const SwitchUD1U8P(),
    10 => const SwitchUD1U10P(),
    12 => const SwitchUD1U12P(),
    14 => const SwitchUD1U14P(),
    16 => const SwitchUD1U16P(),
    18 => const SwitchUD1U18P(),
    20 => const SwitchUD1U20P(),
    22 => const SwitchUD1U22P(),
    24 => const SwitchUD1U24P(),
    26 => const SwitchUD1U26P(),
    28 => const SwitchUD1U28P(),
    _ => const SwitchUD1U24P(),
  };
}
